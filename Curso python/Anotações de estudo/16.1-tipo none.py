"""
Tipo none

O tipo de dado none em python representa o tipo sem tipo, ou poderia ser conhecido tambem como tipo vazio porem, falar que é um tipo sem tipo é mais apropriado.

OBS: o tipo none é SEMPRE especificado com a primeira letra maiuscula

Certo: None
Errado: none

Quando utilizamos?

-Podemos utilizar None quando queremos criar uma variavel e inicializa-la com um tipo, antes de recebermos um valor final

OBS: O tipo None em python é sempre considerado False

"""

numeros= None

print(numeros)
print(type(numeros))

numeros = 1.44, 1, 4.3

print(numeros)
print(type(numeros))
